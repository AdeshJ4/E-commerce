const mongoose = require('mongoose');

// each user have only one cart which contain all products he cart in his bucket.
const cartSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  items: [
    {
      productId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Product',
        required: true
      },
      quantity: {
        type: Number,
        required: true,
        min: 1
      }
    }
  ]
}, {timestamps: true});


module.exports = mongoose.model("Cart", cartSchema);